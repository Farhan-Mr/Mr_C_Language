/* WAP to find Area of Rectangle */

#include<stdio.h>
int main (){

    int l,w;
    printf("Enter length of rectangle - ");
    scanf("%d",&l);

    printf("Enter width of rectangle - ");
    scanf("%d",&w);
    
    // Area of Rectangle l*w 

    printf("Area of Rectangle : %d",l*w);
    return 0;
}